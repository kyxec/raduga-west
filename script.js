/* ==========================================================================
   LANGUAGE TRANSITION ENGINE
   ========================================================================== */
let currentLanguage = localStorage.getItem('guest_lang') || 'ru';

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLanguage = lang;
    localStorage.setItem('guest_lang', lang);
    
    // Update Meta Title & Description
    document.title = translations[lang].meta_title;
    const metaDescTag = document.getElementById('meta-desc');
    if (metaDescTag) {
        metaDescTag.setAttribute('content', translations[lang].meta_desc);
    }
    
    // Update text content of translating elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Update active visual language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// Language switch click events
document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
        const targetLang = btn.getAttribute('data-lang');
        setLanguage(targetLang);
    });
});

/* ==========================================================================
   INTERSECTION OBSERVER (SCROLL HIGHLIGHTS IN NAVIGATION)
   ========================================================================== */
const sections = document.querySelectorAll('.section, .hero');
const navLinks = document.querySelectorAll('.nav-link');

const observerOptions = {
    root: null,
    rootMargin: '-30% 0px -60% 0px', // Center-biased threshold for mobile
    threshold: 0
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const id = entry.target.getAttribute('id');
            navLinks.forEach(link => {
                const href = link.getAttribute('href');
                if (href === `#${id}`) {
                    link.classList.add('active-section');
                    link.scrollIntoView({ behavior: 'smooth', block: 'nearest', inline: 'center' });
                } else {
                    link.classList.remove('active-section');
                }
            });
        }
    });
}, observerOptions);

sections.forEach(section => observer.observe(section));

// Smooth navigation scrolling
navLinks.forEach(link => {
    link.addEventListener('click', (e) => {
        e.preventDefault();
        const targetId = link.getAttribute('href');
        const targetSection = document.querySelector(targetId);
        
        if (targetSection) {
            const headerOffset = 110;
            const elementPosition = targetSection.getBoundingClientRect().top + window.scrollY;
            const offsetPosition = elementPosition - headerOffset;
            
            window.scrollTo({
                top: offsetPosition,
                behavior: 'smooth'
            });
        }
    });
});

